package arrays.onedimensional;

// Accessing array elements --> Array elements can be access using indexing.

public class accessingArray {
    public static void main(String[] args) {
        //creating array objects.

        String[] Drinks = {"Coke", "Fanta", "Sprite", "Redbull"};
        int[] numbers = {23, 43, 35, 65,67, 34, 54};

        //accessing array elements using indexing
        System.out.println(Drinks[3]);
        System.out.println(numbers[2]);
    }
}
