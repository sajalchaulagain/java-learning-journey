package arrays.onedimensional;


// Length of an array --> done using length property.


public class lengthArray {
    public static void main(String[] args) {
        //creating array objects.

        String[] cities = {"Kathmandu", "Pokhara", "Chitwan"};
        int[] numbers = {12, 35, 65, 64, 245, 656,45,56,86};

        System.out.println("Number of cities : " + cities.length);
        System.out.println("length of numbers : " + numbers.length);
    }
}
