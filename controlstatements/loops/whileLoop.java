package controlstatements.loops;


// Print numbers from 1 to 5


public class whileLoop {
    public static void main(String[] args) {
        int i = 1;

        while(i <= 5){
            System.out.println(i);
            i++;
        }
    }
}

//Runs while the condition is true.

